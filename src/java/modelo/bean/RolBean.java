package modelo.bean;

/**
 *
 * @author Laura Arvizu
 */
public class RolBean {

    private int idRol;
    private String rol;

    public RolBean() {
        
    }

    public RolBean(int idRol, String rol) {
        this.idRol = idRol;
        this.rol = rol;
    }

    public int getIdRol() {
        return idRol;
    }

    public void setIdRol(int idRol) {
        this.idRol = idRol;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

}
